class Student:
    def __init__(self, class_code, student_id, name, birth,
                 gender, address, phone, email, register_date):
        self.class_code = class_code
        self.student_id = student_id
        self.name = name
        self.birth = birth
        self.gender = gender
        self.address = address
        self.phone = phone
        self.email = email
        self.register_date = register_date
